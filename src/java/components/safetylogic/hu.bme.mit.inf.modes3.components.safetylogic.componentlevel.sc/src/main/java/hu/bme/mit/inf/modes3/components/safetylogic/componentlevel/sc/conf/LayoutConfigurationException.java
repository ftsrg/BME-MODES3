package hu.bme.mit.inf.modes3.components.safetylogic.componentlevel.sc.conf;

public class LayoutConfigurationException extends Exception {

	private static final long serialVersionUID = 7222982657197300763L;

	public LayoutConfigurationException() {
		super();
	}

	public LayoutConfigurationException(String message) {
		super(message);
	}

	public LayoutConfigurationException(Throwable cause) {
		super(cause);
	}

	public LayoutConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

}
